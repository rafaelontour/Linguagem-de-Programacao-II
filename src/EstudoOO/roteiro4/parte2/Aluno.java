package EstudoOO.roteiro4.parte2;

public class Aluno {
    public int matricula;
    public String nome;
    public String curso;
    public int anoIngresso;

    public Aluno(int pMatricula, String pNome, String pCurso, int pAnoIngresso) {
        this.matricula = pMatricula;
        this.nome = pNome;
        this.curso = pCurso;
        this.anoIngresso = pAnoIngresso;
    }
}
